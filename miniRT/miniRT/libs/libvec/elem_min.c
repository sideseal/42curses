#include "libvec.h"

t_vec3	elem_min(t_vec3 u, t_vec3 v)
{
	if (v.x < u.x)
		u.x = v.x;
	if (v.y < u.y)
		u.y = v.y;
	if (v.z < u.z)
		u.z = v.z;
	return (u);
}
