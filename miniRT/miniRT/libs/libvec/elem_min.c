#include "libvec.h"

t_vec3	elem_min(t_vec3 u, t_vec3 v)
{
	if (v.ai < u.ai)
		u.ai = v.ai;
	if (v.bj < u.bj)
		u.bj = v.bj;
	if (v.ck < u.ck)
		u.ck = v.ck;
	return (u);
}
