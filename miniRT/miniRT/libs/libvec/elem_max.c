#include "libvec.h"

t_vec3	elem_max(t_vec3 u, t_vec3 v)
{
	if (u.x < v.x)
		u.x = v.x;
	if (u.y < v.y)
		u.y = v.y;
	if (u.z < v.z)
		u.z = v.z;
	return (u);
}
