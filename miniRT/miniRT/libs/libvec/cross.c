#include "libvec.h"

t_vec3	cross(t_vec3 u, t_vec3 v)
{
	t_vec3	ret;

	ret.x = u.y * v.z - u.z * v.y;
	ret.y = u.z * v.x - u.x * v.z;
	ret.z = u.x * v.y - u.y * v.x;
	return (ret);
}
